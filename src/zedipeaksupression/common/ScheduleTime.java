package zedipeaksupression.common;

class ScheduleTime {
	private int day;
	private int time;
	
	public ScheduleTime(int d, int t) {
		this.day=d;
		this.time=t;
	}
	
	public int getDay() {
		return day;
	}
	public void setDay(int day) {
		this.day = day;
	}
	public int getTime() {
		return time;
	}
	public void setTime(int time) {
		this.time = time;
	}
	
		

}
